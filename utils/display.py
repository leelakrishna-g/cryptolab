def print_result(label, value):
    # Prints label: value in consistent format
    print(f"    {label:<15}: {value}")

def print_separator():
    # Prints a line divider
    print("═" * 50)

def print_header(title):
    # Prints a clean divider with title
    print_separator()
    print(f"  {title}")
    print_separator()


